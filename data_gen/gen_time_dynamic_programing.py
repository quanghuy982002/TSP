import time

def tsp(matrix):
    n = len(matrix)
    dp = {}

    def get_minimum(k, a):
        if (k, a) in dp:
            return dp[k, a]

        if not a:
            return matrix[k][0]

        values = []
        for j in a:
            set_a = tuple(x for x in a if x != j)
            if (j, set_a) not in dp:
                result = matrix[k][j] + get_minimum(j, set_a)
                dp[j, set_a] = result
            values.append(dp[j, set_a])

        dp[k, a] = min(values)
        return dp[k, a]

    optimal_cost = get_minimum(0, tuple(range(1, n)))

    path = [0]
    a = tuple(range(1, n))
    while a:
        j = min(a, key=lambda x: matrix[path[-1]][x] + dp[x, tuple(y for y in a if y != x)])
        path.append(j)
        a = tuple(x for x in a if x != j)

    path.append(0)

    return optimal_cost, path


def run_tsp(matrix):
    max_cities = len(matrix)
    num_cities_list = []  # Mảng số lượng thành phố
    execution_time_list = []  # Mảng thời gian chạy

    for num_cities in range(1, max_cities + 1):
        sub_matrix = matrix[:num_cities][:num_cities]

        start_time = time.time()
        optimal_cost, optimal_path = tsp(sub_matrix)
        end_time = time.time()
        execution_time = end_time - start_time

        num_cities_list.append(num_cities)
        execution_time_list.append(execution_time)

        print("Number of Cities:", num_cities)
        print("Optimal Cost:", optimal_cost)
        print("Optimal Path:", optimal_path)
        print("Execution Time:", execution_time, "seconds")
        print("--------------------------------------------")

    return num_cities_list, execution_time_list


